package icu.gieok.vo;

public class AttrLikeVO {
	
	private int user_code;
	private int attr_code;
	private String attr_like;
	
	
	public int getUser_code() {
		return user_code;
	}
	public void setUser_code(int user_code) {
		this.user_code = user_code;
	}
	public int getAttr_code() {
		return attr_code;
	}
	public void setAttr_code(int attr_code) {
		this.attr_code = attr_code;
	}
	public String getAttr_like() {
		return attr_like;
	}
	public void setAttr_like(String attr_like) {
		this.attr_like = attr_like;
	}
	
	
	

}
