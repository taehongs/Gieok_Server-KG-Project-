package icu.gieok.vo;

public class WithVO {
	
	private int with_no;  // 동행 번호
	private int board_no;  // 동행 게시판 글 번호
	private String board_writer;  // 동행 작성자
	private String with_user_id; //신청자
    private int with_user_code; // 신청자 코드
    private String with_user_info; // 신청 문구
    private String with_accept;  // 수락 여부
    
    
	public int getWith_no() {
		return with_no;
	}
	public void setWith_no(int with_no) {
		this.with_no = with_no;
	}
	public int getBoard_no() {
		return board_no;
	}
	public void setBoard_no(int board_no) {
		this.board_no = board_no;
	}
	public String getBoard_writer() {
		return board_writer;
	}
	public void setBoard_writer(String board_writer) {
		this.board_writer = board_writer;
	}
	public String getWith_user_id() {
		return with_user_id;
	}
	public void setWith_user_id(String with_user_id) {
		this.with_user_id = with_user_id;
	}
	public int getWith_user_code() {
		return with_user_code;
	}
	public void setWith_user_code(int with_user_code) {
		this.with_user_code = with_user_code;
	}
	public String getWith_user_info() {
		return with_user_info;
	}
	public void setWith_user_info(String with_user_info) {
		this.with_user_info = with_user_info;
	}
	public String getWith_accept() {
		return with_accept;
	}
	public void setWith_accept(String with_accept) {
		this.with_accept = with_accept;
	}
	

	

}
